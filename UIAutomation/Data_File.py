#整型
x = 5
y= 5
z = x+y
print(z)
#浮点型
f = 5.30
l = 5.20
print(f+l)
#字符串 用单引号 或者双引号
str = 'hello 51zxw'
print(str)
#转义字符 \n换行 \\转义（路径用）
a = "hello \n 51zxw"
print(a)
print("hello c:\\ 51zxw")
# my name is 'jack' and "you" 单双引号的转义
print('my name is \'jack\' and \"you\"')
#布尔值
t = True
f = False
print(t and f)
print(t or f)
